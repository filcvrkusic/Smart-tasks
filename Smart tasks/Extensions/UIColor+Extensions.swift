import UIKit

extension UIColor {
    static func lightYellow() -> UIColor {
        return UIColor(red: 255.0/255.0, green: 222.0/255.0, blue: 97.0/255.0, alpha: 1.0)
    }
    
    static func darkCyan() -> UIColor {
        UIColor(red: 2.0/255.0, green: 137.0/255.0, blue: 123.0/255.0, alpha: 1.0)
    }
    
    static func lightOrange() -> UIColor {
        UIColor(red: 246.0/255.0, green: 239.0/255.0, blue: 222.0/255.0, alpha: 1.0)
    }
    
    static func softRed() -> UIColor {
        UIColor(red: 239.0/255.0, green: 75.0/255.0, blue: 94.0/255.0, alpha: 1.0)
    }
}
